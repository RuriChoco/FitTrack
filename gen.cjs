const fileContent = `// THIS FILE IS AUTOMATICALLY GENERATED
import { type ActivityLog, type Achievement } from '../api';

export const ALL_ACHIEVEMENTS: Achievement[] = [
${achievements.join(',\n')}
];

const achMap = Object.fromEntries(ALL_ACHIEVEMENTS.map(a => [a.id, a]));

export function evaluateAchievements(logs: ActivityLog[]): Achievement[] {
  const count = logs.length;
  const dur = logs.reduce((sum, doc) => sum + doc.duration, 0);
  const uniqueExercises = new Set(logs.map(l => l.exercise_name)).size;
  const maxSession = logs.reduce((max, l) => Math.max(max, l.duration), 0);
  
  const exerciseCounts: Record<string, number> = {};
  logs.forEach(l => {
    exerciseCounts[l.exercise_name] = (exerciseCounts[l.exercise_name] || 0) + 1;
  });

  const weekendCount = logs.filter(l => {
    const [y, m, d] = l.date.split('-').map(Number);
    const dayOfWeek = new Date(y, m - 1, d).getDay();
    return dayOfWeek === 0 || dayOfWeek === 6;
  }).length;

  const uniqueDates = Array.from(new Set(logs.map(l => l.date))).sort();
  let maxStreak = 0;
  let curStr = 0;
  for (let i = 0; i < uniqueDates.length; i++) {
    if (i === 0) curStr = 1;
    else {
      const [y1, m1, d1] = uniqueDates[i-1].split('-').map(Number);
      const [y2, m2, d2] = uniqueDates[i].split('-').map(Number);
      const diffDays = Math.round((new Date(y2, m2 - 1, d2).getTime() - new Date(y1, m1 - 1, d1).getTime()) / (1000 * 3600 * 24));
      if (diffDays === 1) curStr++;
      else curStr = 1;
    }
    maxStreak = Math.max(maxStreak, curStr);
  }

  const unlocked: Achievement[] = [];
${logic.join('\n')}

  return unlocked;
}
`;

