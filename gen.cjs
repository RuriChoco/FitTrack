const fs = require('fs');

const exercises = [
  'Brisk Walking', 'Gentle Yoga', 'Swimming', 'Chair Aerobics',
  'Resistance Band Training', 'Tai Chi', 'Bodyweight Squats',
  'Stretching Routine', 'Dancing', 'Pilates', 'Heavy Lifting'
];

let achievements = [];
let logic = [];

function addAchievement(id, name, desc, icon, condition) {
  achievements.push(`  { id: '${id}', name: '${name.replace(/'/g, "\\'")}', description: '${desc.replace(/'/g, "\\'")}', icon: '${icon}' }`);
  logic.push(`  if (${condition}) unlocked.push(achMap['${id}']);`);
}

// 1. Total Count (10)
const counts = [1, 5, 10, 25, 50, 100, 250, 500, 1000, 2000];
counts.forEach(c => addAchievement(`count_${c}`, `Activity Level ${c}`, `Log ${c} total activities`, 'Star', `count >= ${c}`));

// 2. Active Minutes (10)
const mins = [50, 100, 500, 1000, 2500, 5000, 10000, 25000, 50000, 100000];
mins.forEach(m => addAchievement(`min_${m}`, `Time Master ${m}`, `Reach ${m} active minutes`, 'Award', `dur >= ${m}`));

// 3. Unique Exercises (5)
const uniques = [3, 5, 10, 15, 20];
uniques.forEach(u => addAchievement(`unique_${u}`, `Variety Pack ${u}`, `Log ${u} different types of exercises`, 'Medal', `uniqueExercises >= ${u}`));

// 4. Longest Session (6)
const sessions = [60, 90, 120, 180, 240, 300];
sessions.forEach(s => addAchievement(`session_${s}`, `Endurance ${s}`, `Log a single activity of ${s}+ minutes`, 'Trophy', `maxSession >= ${s}`));

// 5. Streaks (8)
const streaks = [3, 7, 14, 30, 60, 100, 200, 365];
streaks.forEach(s => addAchievement(`streak_${s}`, `Streak ${s}`, `Log activities ${s} days in a row`, 'Flame', `maxStreak >= ${s}`));

// 6. Specific Exercises (11 * 5 = 55)
const exCounts = [1, 5, 20, 50, 100];
exercises.forEach(ex => {
  exCounts.forEach(c => {
    let safename = ex.toLowerCase().replace(/[^a-z0-9]/g, '_');
    addAchievement(`ex_${safename}_${c}`, `${ex} Fanatic ${c}`, `Log ${ex} ${c} times`, 'Star', `(exerciseCounts['${ex}'] || 0) >= ${c}`);
  });
});

// 7. Weekend Warrior (5)
const weekendCounts = [1, 5, 20, 50, 100];
weekendCounts.forEach(w => addAchievement(`weekend_${w}`, `Weekend Warrior ${w}`, `Log ${w} weekend activities`, 'Medal', `weekendCount >= ${w}`));

// Legacy defaults
addAchievement('first_step', 'First Step', 'Log your first activity', 'Star', 'count >= 1');
addAchievement('getting_serious', 'Getting Serious', 'Log 5 total activities', 'Medal', 'count >= 5');
addAchievement('century_club', 'Century Club', 'Reach 100 active minutes', 'Award', 'dur >= 100');
